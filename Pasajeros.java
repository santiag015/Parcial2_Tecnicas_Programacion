class Pasajeros {
    //Atributos
  private String nom;
  private String preferenciaComida;
  private int numPasaporte;
  private int edad;

  
  
  
    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getPreferenciaComida() {
        return preferenciaComida;
    }

    public void setPreferenciaComida(String preferenciaComida) {
        this.preferenciaComida = preferenciaComida;
    }

    public int getNumPasaporte() {
        return numPasaporte;
    }

    public void setNumPasaporte(int numPasaporte) {
        this.numPasaporte = numPasaporte;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
    
}
